package com.napier.sem.classes;

public class country
{
    /**
     * Countries code
     */
    public String Code;
    /**
     * Countries names
     */
    public String Name;
    /**
     * Continent of the country, this is an enum in the database
     */
    public String Continent;
    /**
     * Continent of the country
     */
    public String Region;
    /**
     * Surface area of the country
     */
    public float SurfaceArea;
    /**
     * Countries year of independence
     */
    public int IndepYear;
    /**
     * Countries population
     */
    public int Population;
    /**
     * The countries life expectancy of the population
     */
    public float LifeExpectancy;
    /**
     * A Countries goods and services produced per yr, plus foreign investments
     */
    public float GNP;
    /**
     * Past GNP
     */
    public float GNPOld;
    /**
     * Countries name in local population/native language
     */
    public String LocalName;
    /**
     * Type of government/ tied to another government
     */
    public String GovernmentForm;
    /**
     * Countries public leading figure
     */
    public String HeadOfState;
    /**
     * A countries political/ economic hub
     */
    public int Capital;
    /**
     * ?
     */
    public String Code2;
}
